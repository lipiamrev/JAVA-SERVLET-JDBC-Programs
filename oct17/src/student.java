
public class student {
	int id;
	String name;
	double percentage;
	
	




	public student(int id, String name, double percentage) {
		super();
		this.id = id;
		this.name = name;
		this.percentage = percentage;
	}






	void display() {
		System.out.println("Id is "+id+" Name is "+name+" and Percentage is "+percentage);
	}
}
